package customer360.apiPojo.apiResp.email;

public class Graph {

	private String average;
	private Day mon;
	private Day tue;
	private Day wed;
	private Day thur;
	private Day fri;
	private Day weekend;

	public String getAverage() {
		return average;
	}

	public void setAverage(String average) {
		this.average = average;
	}

	public Day getMon() {
		return mon;
	}

	public void setMon(Day mon) {
		this.mon = mon;
	}

	public Day getTue() {
		return tue;
	}

	public void setTue(Day tue) {
		this.tue = tue;
	}

	public Day getWed() {
		return wed;
	}

	public void setWed(Day wed) {
		this.wed = wed;
	}

	public Day getThur() {
		return thur;
	}

	public void setThur(Day thur) {
		this.thur = thur;
	}

	public Day getFri() {
		return fri;
	}

	public void setFri(Day fri) {
		this.fri = fri;
	}

	public Day getWeekend() {
		return weekend;
	}

	public void setWeekend(Day weekend) {
		this.weekend = weekend;
	}
}
